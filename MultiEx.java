
	public class MultiEx extends Thread{
		public void run()
		{
			for(int i=0;i<5;i++)
			{
				try
				{
					Thread.sleep(500);
					System.out.println(i);
				}
				catch(InterruptedException e)
				{
					System.out.println(e);
				}
			}
		}
	}
	
	
	
